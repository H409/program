//=============================================================================
// set size
//=============================================================================
bool BaseWindow::SetSize(const u16& in_width,const u16& in_height)
{
	if(!SetSize_(in_width,in_height))
	{
		return false;
	}

	width_ = in_width;
	height_ = in_height;

	return true;
}

