//=============================================================================
// 出力
//=============================================================================
void CDebugProcedure::Print(const char* format,...)
{
#ifdef _DEBUG
	char work[STRING_MAX] = {NULL};
	va_list args;

	// 可変引数の参照開始
	va_start(args,format);

	// 文字列に変更
	vsprintf(work,format,args);

	// 可変引数の参照終了
	va_end(args);

	// ログに書込
	strcat(string_,work);
#endif
}

