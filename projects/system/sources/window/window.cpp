#include "window.h"

namespace window
{
//=============================================================================
// constructor
//=============================================================================
Window::Window(void)
	:width_(0)
	,height_(0)
{
}

//=============================================================================
// destructor
//=============================================================================
Window::~Window(void)
{
}

//=============================================================================
// set size
//=============================================================================
bool Window::SetSize(const u16& in_width,const u16& in_height)
{
	if(!SetSize_(in_width,in_height))
	{
		return false;
	}

	width_ = in_width;
	height_ = in_height;

	return true;
}

} // namespace window

